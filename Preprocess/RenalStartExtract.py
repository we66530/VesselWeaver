# 7. Core Processing
# ============================================================
def process_one_task(
    task: StartMaskTask,
    case_id: str,
    ct_data: np.ndarray,
    output_points: np.ndarray,
) -> List[Dict]:
    input_path = build_task_input_path(case_id, task)

    print("\n" + "=" * 100)
    print(f"[PROCESS TASK] {task.name}")
    print(f"[INPUT] {input_path}")
    print("=" * 100)

    mask_img, mask_data = load_nifti(input_path)
    check_same_shape("ROI_CT", ct_data, task.name, mask_data)

    binary_mask = mask_data > 0
    total_voxels = int(binary_mask.sum())

    print(f"[INFO] Positive voxels: {total_voxels:,}")

    if total_voxels == 0:
        print(f"[WARNING] Empty mask for task: {task.name}")
        return []

    labeled, num_components = ndimage.label(binary_mask, structure=CONNECTIVITY_STRUCTURE)
    print(f"[INFO] Connected components found: {num_components}")

    records: List[Dict] = []

    for component_id in range(1, num_components + 1):
        component_mask = labeled == component_id
        component_coords = np.argwhere(component_mask)
        component_voxels = int(component_coords.shape[0])

        centroid = compute_component_centroid(component_coords)
        rounded_centroid = round_centroid_to_voxel(centroid)

        start_point, selection_method = select_valid_start_point(
            ct_data=ct_data,
            component_coords=component_coords,
            centroid=centroid,
        )

        if start_point is None:
            print(
                f"[WARNING] {task.name} component {component_id}: "
                "no valid HU voxel found. Skipped."
            )
            records.append({
                "task_name": task.name,
                "component_id": component_id,
                "component_voxels": component_voxels,
                "centroid_x": float(centroid[0]),
                "centroid_y": float(centroid[1]),
                "centroid_z": float(centroid[2]),
                "rounded_x": rounded_centroid[0],
                "rounded_y": rounded_centroid[1],
                "rounded_z": rounded_centroid[2],
                "point_x": "NA",
                "point_y": "NA",
                "point_z": "NA",
                "point_hu": "NA",
                "output_label": 0,
                "selection_method": selection_method,
                "status": "failed",
            })
            continue

        point_hu = float(ct_data[start_point])
        output_label = int(task.output_label_base + component_id)

        draw_sphere(
            output=output_points,
            center=start_point,
            radius=POINT_SPHERE_RADIUS,
            label_value=output_label,
        )

        print(
            f"[OK] {task.name} component {component_id}: "
            f"voxels={component_voxels:,}, centroid={np.round(centroid, 2).tolist()}, "
            f"point={start_point}, HU={point_hu:.1f}, label={output_label}, method={selection_method}"
        )

        records.append({
            "task_name": task.name,
            "component_id": component_id,
            "component_voxels": component_voxels,
            "centroid_x": float(centroid[0]),
            "centroid_y": float(centroid[1]),
            "centroid_z": float(centroid[2]),
            "rounded_x": rounded_centroid[0],
            "rounded_y": rounded_centroid[1],
            "rounded_z": rounded_centroid[2],
            "point_x": start_point[0],
            "point_y": start_point[1],
            "point_z": start_point[2],
            "point_hu": point_hu,
            "output_label": output_label,
            "selection_method": selection_method,
            "status": "ok",
        })

    return records


def run_renal_start_extract(case_id: str) -> Tuple[Path, Path, Path]:
    ct_path = build_roi_ct_path(case_id)
    output_points_path = build_output_points_path(case_id)
    output_txt_path = build_output_txt_path(case_id)
    output_summary_path = build_output_summary_path(case_id)

    print("=" * 100)
    print("[RENAL START EXTRACT - RUN.PY VERSION]")
    print(f"[CASE ID] {case_id}")
    print(f"[ROI CT ] {ct_path}")
    print(f"[OUTPUT ] {output_points_path}")
    print(f"[TXT    ] {output_txt_path}")
    print(f"[SUMMARY] {output_summary_path}")
    print("=" * 100)

    ensure_dir(ANCHOR_POINTS_ROOT)

    if SKIP_IF_FINAL_EXISTS and output_points_path.exists() and output_txt_path.exists() and output_summary_path.exists():
        print(f"[SKIP] Output already exists: {output_points_path}")
        print(f"[SKIP] TXT already exists: {output_txt_path}")
        print(f"[SKIP] Summary already exists: {output_summary_path}")
        return output_points_path, output_txt_path, output_summary_path

    ct_img, ct_data = load_nifti(ct_path)
    print(f"[INFO] ROI CT shape: {ct_data.shape}")
    print(f"[INFO] ROI CT HU range: min={ct_data.min():.1f}, max={ct_data.max():.1f}")

    output_points = np.zeros(ct_data.shape, dtype=np.uint16)

    all_records: List[Dict] = []
    for task in TASKS:
        records = process_one_task(
            task=task,
            case_id=case_id,
            ct_data=ct_data,
            output_points=output_points,
        )
        all_records.extend(records)

    output_header = ct_img.header.copy()
    output_header.set_data_dtype(np.uint16)

    output_img = nib.Nifti1Image(
        output_points.astype(np.uint16),
        affine=ct_img.affine,
        header=output_header,
    )
    nib.save(output_img, str(output_points_path))

    if not output_points_path.exists():
        raise RuntimeError(f"Output was not created: {output_points_path}")

    # Full point table.
    txt_header = [
        "task_name",
        "component_id",
        "component_voxels",
        "centroid_x",
        "centroid_y",
        "centroid_z",
        "rounded_x",
        "rounded_y",
        "rounded_z",
        "point_x",
        "point_y",
        "point_z",
        "point_hu",
        "output_label",
        "selection_method",
        "status",
    ]

    with open(output_txt_path, "w", encoding="utf-8") as f:
        f.write("\t".join(txt_header) + "\n")
        for r in all_records:
            row = []
            for key in txt_header:
                value = r.get(key, "")
                if isinstance(value, float):
                    row.append(f"{value:.6f}")
                else:
                    row.append(str(value))
            f.write("\t".join(row) + "\n")

    # Compact summary.
    with open(output_summary_path, "w", encoding="utf-8") as f:
        f.write(f"case_id\t{case_id}\n")
        f.write(f"roi_ct_path\t{ct_path}\n")
        f.write(f"output_points_path\t{output_points_path}\n")
        f.write(f"output_txt_path\t{output_txt_path}\n")
        f.write(f"invalid_hu_value\t{INVALID_HU_VALUE}\n")
        f.write(f"point_sphere_radius\t{POINT_SPHERE_RADIUS}\n")
        f.write(f"max_nearby_search_radius\t{MAX_NEARBY_SEARCH_RADIUS}\n")
        f.write("\n")
        f.write("task_name\tinput_path\tcomponents_total\tpoints_ok\tpoints_failed\n")

        for task in TASKS:
            task_records = [r for r in all_records if r["task_name"] == task.name]
            ok = sum(1 for r in task_records if r["status"] == "ok")
            failed = sum(1 for r in task_records if r["status"] != "ok")
            f.write(
                f"{task.name}\t{build_task_input_path(case_id, task)}\t"
                f"{len(task_records)}\t{ok}\t{failed}\n"
            )

    print("\n" + "=" * 100)
    print("[DONE] Renal start extraction completed.")
    print(f"[OUTPUT POINTS] {output_points_path}")
    print(f"[OUTPUT TXT   ] {output_txt_path}")
    print(f"[SUMMARY      ] {output_summary_path}")
    print(f"[INFO] Output labels present: {[int(x) for x in np.unique(output_points) if x > 0]}")
    print("=" * 100)

    return output_points_path, output_txt_path, output_summary_path


# ============================================================
# 8. Command-Line Interface for run.py
# ============================================================
def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Extract centroid start points from renal start restrict masks and validate against ROI_CT HU."
    )

    parser.add_argument(
        "--ct_path",
        required=True,
        help="CT path passed by run.py. Accepted for compatibility; this script uses ROI_CT output.",
    )

    parser.add_argument(
        "--output_dir",
        required=True,
        help="Output directory passed by run.py. Accepted for compatibility; output is written to AnchorPoints.",
    )

    parser.add_argument(
        "--case_id",
        required=False,
        default=None,
        help="Case ID passed by run.py. If omitted, inferred from CT filename.",
    )

    return parser.parse_args()


# ============================================================
# 9. Main Entry Point
# ============================================================
def main() -> None:
    args = parse_args()
    ct_path = Path(args.ct_path).resolve()

    case_id = args.case_id.strip() if args.case_id else strip_nii_extension(ct_path.name)
    if not case_id:
        raise ValueError("case_id is empty.")

    run_renal_start_extract(case_id=case_id)


if __name__ == "__main__":
    main()
