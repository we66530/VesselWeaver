# ============================================================
# 4. Utility Functions
# ============================================================
def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def strip_nii_extension(filename: str) -> str:
    lower = filename.lower()
    if lower.endswith(".nii.gz"):
        return filename[:-7]
    if lower.endswith(".nii"):
        return filename[:-4]
    return Path(filename).stem


def load_nifti(path: Path):
    if not path.exists():
        raise FileNotFoundError(f"File not found:\n{path}")
    img = nib.load(str(path))
    data = img.get_fdata()
    return img, data


def check_same_shape(reference_name: str, reference_data: np.ndarray, target_name: str, target_data: np.ndarray) -> None:
    if reference_data.shape != target_data.shape:
        raise ValueError(
            f"Shape mismatch: {reference_name} shape {reference_data.shape} "
            f"!= {target_name} shape {target_data.shape}"
        )


def is_inside_shape(point: Tuple[int, int, int], shape: Tuple[int, int, int]) -> bool:
    x, y, z = point
    sx, sy, sz = shape
    return 0 <= x < sx and 0 <= y < sy and 0 <= z < sz


# ============================================================
# 5. Centroid and Valid-HU Point Selection
# ============================================================
def compute_component_centroid(component_coords: np.ndarray) -> np.ndarray:
    return component_coords.astype(np.float64).mean(axis=0)


def round_centroid_to_voxel(centroid: np.ndarray) -> Tuple[int, int, int]:
    rounded = np.rint(centroid).astype(np.int64)
    return tuple(int(v) for v in rounded.tolist())


def find_nearest_valid_voxel_inside_component(
    ct_data: np.ndarray,
    component_coords: np.ndarray,
    centroid: np.ndarray,
    invalid_hu_value: float = INVALID_HU_VALUE,
) -> Optional[Tuple[int, int, int]]:
    """
    Prefer a voxel inside the same connected component.
    Pick the component voxel closest to centroid, but require HU != -1024.
    If multiple voxels are similarly close, this naturally picks the closest one.
    """
    if component_coords.size == 0:
        return None

    hu_values = ct_data[
        component_coords[:, 0],
        component_coords[:, 1],
        component_coords[:, 2],
    ]

    valid_mask = hu_values != invalid_hu_value
    if not np.any(valid_mask):
        return None

    valid_coords = component_coords[valid_mask]
    distances = np.linalg.norm(valid_coords.astype(np.float64) - centroid[None, :], axis=1)
    best_idx = int(np.argmin(distances))
    best = valid_coords[best_idx]

    return tuple(int(v) for v in best.tolist())


def find_nearest_valid_voxel_near_centroid(
    ct_data: np.ndarray,
    centroid: np.ndarray,
    invalid_hu_value: float = INVALID_HU_VALUE,
    max_radius: int = MAX_NEARBY_SEARCH_RADIUS,
) -> Optional[Tuple[int, int, int]]:
    """
    Fallback search around the rounded centroid.
    This is used if the entire connected component overlaps invalid ROI_CT voxels.
    """
    shape = np.array(ct_data.shape, dtype=np.int64)
    center = np.rint(centroid).astype(np.int64)

    for radius in range(0, max_radius + 1):
        x_min = max(0, int(center[0]) - radius)
        x_max = min(int(shape[0]) - 1, int(center[0]) + radius)
        y_min = max(0, int(center[1]) - radius)
        y_max = min(int(shape[1]) - 1, int(center[1]) + radius)
        z_min = max(0, int(center[2]) - radius)
        z_max = min(int(shape[2]) - 1, int(center[2]) + radius)

        sub_ct = ct_data[x_min:x_max + 1, y_min:y_max + 1, z_min:z_max + 1]
        valid = sub_ct != invalid_hu_value

        if not np.any(valid):
            continue

        local_coords = np.argwhere(valid)
        global_coords = local_coords + np.array([x_min, y_min, z_min], dtype=np.int64)
        distances = np.linalg.norm(global_coords.astype(np.float64) - centroid[None, :], axis=1)
        best = global_coords[int(np.argmin(distances))]
        return tuple(int(v) for v in best.tolist())

    return None


def select_valid_start_point(
    ct_data: np.ndarray,
    component_coords: np.ndarray,
    centroid: np.ndarray,
) -> Tuple[Optional[Tuple[int, int, int]], str]:
    rounded = round_centroid_to_voxel(centroid)

    if is_inside_shape(rounded, ct_data.shape) and float(ct_data[rounded]) != INVALID_HU_VALUE:
        return rounded, "rounded_centroid"

    in_component = find_nearest_valid_voxel_inside_component(
        ct_data=ct_data,
        component_coords=component_coords,
        centroid=centroid,
        invalid_hu_value=INVALID_HU_VALUE,
    )
    if in_component is not None:
        return in_component, "nearest_valid_voxel_inside_component"

    nearby = find_nearest_valid_voxel_near_centroid(
        ct_data=ct_data,
        centroid=centroid,
        invalid_hu_value=INVALID_HU_VALUE,
        max_radius=MAX_NEARBY_SEARCH_RADIUS,
    )
    if nearby is not None:
        return nearby, "nearest_valid_voxel_near_centroid"

    return None, "failed_no_valid_hu_voxel"


# ============================================================
# 6. Drawing
# ============================================================
def draw_sphere(
    output: np.ndarray,
    center: Tuple[int, int, int],
    radius: int,
    label_value: int,
) -> None:
    cx, cy, cz = center
    shape = output.shape

    x_min = max(cx - radius, 0)
    x_max = min(cx + radius + 1, shape[0])
    y_min = max(cy - radius, 0)
    y_max = min(cy + radius + 1, shape[1])
    z_min = max(cz - radius, 0)
    z_max = min(cz + radius + 1, shape[2])

    xs = np.arange(x_min, x_max)
    ys = np.arange(y_min, y_max)
    zs = np.arange(z_min, z_max)
    xg, yg, zg = np.meshgrid(xs, ys, zs, indexing="ij")

    dist = np.sqrt((xg - cx) ** 2 + (yg - cy) ** 2 + (zg - cz) ** 2)
    sphere = dist <= radius

    output[x_min:x_max, y_min:y_max, z_min:z_max][sphere] = int(label_value)


