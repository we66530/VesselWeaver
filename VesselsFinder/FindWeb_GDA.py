# 5. Target Anchor Sphere Centroids
# ============================================================
def get_target_points_from_anchor_spheres(anchor_spheres_path: Path, ct_data: np.ndarray) -> List[Tuple[int, int, int]]:
    anchor_img, anchor_data = load_nifti(anchor_spheres_path)
    check_same_shape("ROI CT", ct_data, "anchor spheres", anchor_data)

    labeled_anchors, num_anchors = label(anchor_data > 0)

    if num_anchors == 0:
        raise RuntimeError(f"No anchor sphere component found in: {anchor_spheres_path}")

    centroids = center_of_mass(anchor_data > 0, labeled_anchors, range(1, num_anchors + 1))
    target_points = [tuple(int(x) for x in np.rint(c).astype(np.int64).tolist()) for c in centroids]

    print("=" * 100)
    print("[TARGET POINTS FROM DUODENUM ANCHOR SPHERES]")
    print(f"[N TARGETS] {len(target_points)}")
    for i, p in enumerate(target_points, start=1):
        print(f"  Target {i}: {p}, CT HU={float(ct_data[p]):.1f}")
    print("=" * 100)

    if len(target_points) != 3:
        print(
            f"[WARNING] Expected 3 target anchor spheres, but found {len(target_points)}. "
            "The script will still trace one path per detected target."
        )

    return target_points


