# 9. Main Pipeline Function
# ============================================================
def run_gda_pathfinding(case_id: str, ct_path: Path) -> Tuple[Path, Path]:
    # Use the original CT selected in run.py, not ROI_CT.
    anchor_path = build_anchor_spheres_path(case_id)
    anchor_txt_path = build_anchor_points_txt_path(case_id)
    output_path = build_output_path(case_id)
    summary_path = build_summary_path(case_id)

    print("=" * 100)
    print("[GDA PATHFINDING - RUN.PY VERSION]")
    print(f"[CASE ID] {case_id}")
    print(f"[CT     ] {ct_path}")
    print(f"[ANCHOR] {anchor_path}")
    print(f"[START TXT] {anchor_txt_path}")
    print(f"[OUTPUT] {output_path}")
    print("=" * 100)

    ensure_dir(output_path.parent)

    if SKIP_IF_FINAL_EXISTS and output_path.exists() and summary_path.exists():
        print(f"[SKIP] GDA output already exists: {output_path}")
        print(f"[SKIP] GDA summary already exists: {summary_path}")
        return output_path, summary_path

    ct_img, ct_data = load_nifti(ct_path)
    print(f"[INFO] CT shape: {ct_data.shape}")
    print(f"[INFO] CT HU range: min={ct_data.min():.1f}, max={ct_data.max():.1f}")

    start_points = get_start_points_from_search3231(anchor_txt_path, ct_data)
    target_points = get_target_points_from_anchor_spheres(anchor_path, ct_data)

    path_results = compute_all_start_target_paths(
        ct_data=ct_data,
        start_points=start_points,
        target_points=target_points,
    )

    best_assignment, best_total_cost = choose_best_assignment(
        path_results=path_results,
        num_starts=len(start_points),
        num_targets=len(target_points),
    )

    selected_paths = []
    for target_idx, start_idx in enumerate(best_assignment):
        selected_path = path_results[(start_idx, target_idx)]["path"]
        if selected_path is None:
            raise RuntimeError("Internal error: selected assignment contains a failed path.")
        selected_paths.append(selected_path)

    vessel_web_mask = render_paths_to_labelmap(ct_data, selected_paths)

    output_header = ct_img.header.copy()
    output_header.set_data_dtype(np.uint16)

    output_img = nib.Nifti1Image(
        vessel_web_mask.astype(np.uint16),
        affine=ct_img.affine,
        header=output_header,
    )
    nib.save(output_img, str(output_path))

    if not output_path.exists():
        raise RuntimeError(f"GDA output was not created: {output_path}")

    write_summary(
        summary_path=summary_path,
        case_id=case_id,
        start_points=start_points,
        target_points=target_points,
        best_assignment=best_assignment,
        best_total_cost=best_total_cost,
        path_results=path_results,
    )

    print("=" * 100)
    print("[DONE] GDA pathfinding completed.")
    print(f"[OUTPUT MASK] {output_path}")
    print(f"[SUMMARY    ] {summary_path}")
    print("=" * 100)

    return output_path, summary_path


