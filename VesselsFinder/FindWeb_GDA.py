# 8. Output Rendering
# ============================================================
def render_paths_to_labelmap(
    ct_data: np.ndarray,
    selected_paths: List[List[Tuple[int, int, int]]],
) -> np.ndarray:
    vessel_web_mask = np.zeros(ct_data.shape, dtype=np.uint16)
    struct = np.ones((3, 3, 3), dtype=bool)

    for i, path in enumerate(selected_paths):
        segment_id = i + 1
        temp_path_mask = np.zeros(ct_data.shape, dtype=bool)

        for p in path:
            temp_path_mask[p] = True

        dilated_path = binary_dilation(
            temp_path_mask,
            structure=struct,
            iterations=PATH_DILATION_ITERATIONS,
        )

        # Preserve earlier labels if overlap occurs.
        write_mask = dilated_path & (vessel_web_mask == 0)
        vessel_web_mask[write_mask] = segment_id

    return vessel_web_mask


def write_summary(
    summary_path: Path,
    case_id: str,
    start_points: List[Tuple[int, int, int]],
    target_points: List[Tuple[int, int, int]],
    best_assignment: List[int],
    best_total_cost: float,
    path_results: Dict[Tuple[int, int], Dict],
) -> None:
    with open(summary_path, "w", encoding="utf-8") as f:
        f.write(f"case_id\t{case_id}\n")
        f.write(f"start_source\t{START_VOLUME_NAME}\n")
        f.write(f"y_limit_max\t{Y_LIMIT_MAX}\n")
        f.write(f"avoid_invalid_hu\t{AVOID_INVALID_HU}\n")
        f.write(f"invalid_hu_value\t{INVALID_HU_VALUE}\n")
        f.write(f"best_total_cost\t{best_total_cost:.6f}\n")
        f.write("\n")

        f.write("START_POINTS\n")
        f.write("start_index\tx\ty\tz\n")
        for i, p in enumerate(start_points, start=1):
            f.write(f"{i}\t{p[0]}\t{p[1]}\t{p[2]}\n")
        f.write("\n")

        f.write("TARGET_POINTS\n")
        f.write("target_index\tx\ty\tz\n")
        for i, p in enumerate(target_points, start=1):
            f.write(f"{i}\t{p[0]}\t{p[1]}\t{p[2]}\n")
        f.write("\n")

        f.write("SELECTED_PATHS\n")
        f.write("path_label\ttarget_index\tstart_index\tstart_x\tstart_y\tstart_z\ttarget_x\ttarget_y\ttarget_z\tcost\tpath_voxels\n")
        for target_idx, start_idx in enumerate(best_assignment):
            start = start_points[start_idx]
            target = target_points[target_idx]
            result = path_results[(start_idx, target_idx)]
            f.write(
                f"{target_idx + 1}\t{target_idx + 1}\t{start_idx + 1}\t"
                f"{start[0]}\t{start[1]}\t{start[2]}\t"
                f"{target[0]}\t{target[1]}\t{target[2]}\t"
                f"{result['cost']:.6f}\t{result['num_voxels']}\n"
            )


