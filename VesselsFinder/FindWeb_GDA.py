# 10. Command-Line Interface for run.py
# ============================================================
def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run GDA pathfinding from search3231 starting points to duodenum anchor spheres using the original CT selected by run.py."
    )

    parser.add_argument(
        "--ct_path",
        required=True,
        help="Original CT path passed by run.py. This CT is used for GDA pathfinding.",
    )

    parser.add_argument(
        "--output_dir",
        required=True,
        help="Output directory passed by run.py. Accepted for compatibility.",
    )

    parser.add_argument(
        "--case_id",
        required=False,
        default=None,
        help="Case ID passed by run.py. If omitted, inferred from CT filename.",
    )

    return parser.parse_args()


# ============================================================
# 11. Main Entry Point
# ============================================================
def main() -> None:
    args = parse_args()
    ct_path = Path(args.ct_path).resolve()

    case_id = args.case_id.strip() if args.case_id else strip_nii_extension(ct_path.name)
    if not case_id:
        raise ValueError("case_id is empty.")

    run_gda_pathfinding(case_id=case_id, ct_path=ct_path)


if __name__ == "__main__":
    main()
