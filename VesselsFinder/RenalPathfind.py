def path_to_mask(shape: Tuple[int, int, int], path: List[Tuple[int, int, int]]) -> np.ndarray:
    mask = np.zeros(shape, dtype=bool)
    for p in path:
        mask[p] = True
    return mask


def build_spacing_exclusion_from_paths(
    shape: Tuple[int, int, int],
    paths: List[List[Tuple[int, int, int]]],
    spacing_voxels: int = MIN_PATH_DISTANCE_VOXELS,
) -> np.ndarray:
    if not paths:
        return np.zeros(shape, dtype=bool)

    base = np.zeros(shape, dtype=bool)
    for path in paths:
        for p in path:
            base[p] = True

    structure = np.ones((3, 3, 3), dtype=bool)
    return binary_dilation(base, structure=structure, iterations=int(spacing_voxels))


def render_paths_to_labelmap(
    shape: Tuple[int, int, int],
    labeled_paths: List[Tuple[int, List[Tuple[int, int, int]]]],
) -> np.ndarray:
    output = np.zeros(shape, dtype=np.uint16)
    structure = np.ones((3, 3, 3), dtype=bool)

    for label_value, path in labeled_paths:
        path_mask = path_to_mask(shape, path)
        dilated = binary_dilation(path_mask, structure=structure, iterations=PATH_DILATION_ITERATIONS)
        write_mask = dilated & (output == 0)
        output[write_mask] = int(label_value)

    return output


