def select_start_point_from_label(
    ct_data: np.ndarray,
    start_data: np.ndarray,
    start_label: int,
    direction: str,
) -> Tuple[int, int, int]:
    start_int = np.rint(start_data).astype(np.int32)
    coords = np.argwhere(start_int == int(start_label))

    if len(coords) == 0:
        available = [int(x) for x in np.unique(start_int) if x > 0]
        raise RuntimeError(
            f"Start label {start_label} was not found in start-point mask.\n"
            f"Available positive labels: {available}\n"
            "Expected labels are 101, 201, 301, and 401 from RenalStartExtract.py."
        )

    hu_values = ct_data[coords[:, 0], coords[:, 1], coords[:, 2]]
    valid_mask = hu_values != INVALID_HU_VALUE

    if not np.any(valid_mask):
        raise RuntimeError(f"Start label {start_label} has no voxel with HU != {INVALID_HU_VALUE}.")

    valid_coords = coords[valid_mask]
    valid_hu = hu_values[valid_mask]

    preferred_mask = valid_hu > START_MIN_HU_PREFERRED
    if np.any(preferred_mask):
        candidate_coords = valid_coords[preferred_mask]
        candidate_hu = valid_hu[preferred_mask]
    else:
        candidate_coords = valid_coords
        candidate_hu = valid_hu

    centroid = candidate_coords.astype(np.float64).mean(axis=0)
    dists = np.linalg.norm(candidate_coords.astype(np.float64) - centroid[None, :], axis=1)

    # Here index-0 is treated as the left-right axis, consistent with your prior scripts.
    x_values = candidate_coords[:, 0]
    if direction.lower() == "right":
        extreme_x = int(np.max(x_values))
    elif direction.lower() == "left":
        extreme_x = int(np.min(x_values))
    else:
        raise ValueError(f"Unsupported direction: {direction}")

    extreme_mask = x_values == extreme_x
    extreme_coords = candidate_coords[extreme_mask]
    extreme_hu = candidate_hu[extreme_mask]
    extreme_dists = dists[extreme_mask]

    # Prefer higher HU, then closer to local centroid.
    order = np.lexsort((extreme_dists, -extreme_hu))
    best = extreme_coords[int(order[0])]
    point = tuple(int(v) for v in best.tolist())

    print(
        f"[START] label={start_label}, direction={direction}, point={point}, "
        f"HU={float(ct_data[point]):.1f}, candidates={len(candidate_coords):,}"
    )
    return point


def find_valid_endpoint_for_hilum_label(
    ct_data: np.ndarray,
    hilum_data: np.ndarray,
    hilum_label: int,
    max_radius: int = ENDPOINT_SEARCH_MAX_RADIUS,
    distance_tolerance: float = ENDPOINT_DISTANCE_TOLERANCE,
) -> Tuple[int, int, int]:
    hilum_int = np.rint(hilum_data).astype(np.int32)
    coords = np.argwhere(hilum_int == int(hilum_label))

    if len(coords) == 0:
        available = [int(x) for x in np.unique(hilum_int) if x > 0]
        raise RuntimeError(
            f"Renal hilum label {hilum_label} was not found.\n"
            f"Available positive labels: {available}\n"
            "Expected label 2 for left renal hilum and label 3 for right renal hilum."
        )

    centroid = coords.astype(np.float64).mean(axis=0)
    rounded = tuple(int(v) for v in np.rint(centroid).astype(np.int64).tolist())

    if is_inside_shape(rounded, ct_data.shape) and float(ct_data[rounded]) != INVALID_HU_VALUE:
        print(
            f"[ENDPOINT] hilum_label={hilum_label}, using rounded centroid={rounded}, "
            f"HU={float(ct_data[rounded]):.1f}"
        )
        return rounded

    print(
        f"[WARNING] Hilum label {hilum_label} rounded centroid {rounded} is invalid. "
        "Searching nearest valid endpoint."
    )

    shape = np.array(ct_data.shape, dtype=np.int64)
    sphere_min = coords.min(axis=0)
    sphere_max = coords.max(axis=0)

    from scipy.spatial import KDTree
    tree = KDTree(coords)

    for radius in range(0, max_radius + 1):
        min_corner = np.maximum(0, sphere_min - radius)
        max_corner = np.minimum(shape - 1, sphere_max + radius)
        sx0, sy0, sz0 = [int(x) for x in min_corner]
        sx1, sy1, sz1 = [int(x) for x in max_corner]

        sub_ct = ct_data[sx0:sx1 + 1, sy0:sy1 + 1, sz0:sz1 + 1]
        valid = sub_ct != INVALID_HU_VALUE

        if not np.any(valid):
            continue

        local_coords = np.argwhere(valid)
        global_coords = local_coords + np.array([sx0, sy0, sz0], dtype=np.int64)
        hu_values = ct_data[global_coords[:, 0], global_coords[:, 1], global_coords[:, 2]]

        distances, _ = tree.query(global_coords)
        min_distance = float(np.min(distances))
        close_mask = distances <= (min_distance + distance_tolerance)

        close_coords = global_coords[close_mask]
        close_hu = hu_values[close_mask]
        close_dist = distances[close_mask]

        # Among similarly close points, prefer higher HU.
        order = np.lexsort((close_dist, -close_hu))
        best_idx = int(order[0])
        best = close_coords[best_idx]
        endpoint = tuple(int(v) for v in best.tolist())

        print(
            f"[ENDPOINT] hilum_label={hilum_label}, fallback={endpoint}, "
            f"HU={float(ct_data[endpoint]):.1f}, distance={float(close_dist[best_idx]):.3f}, radius={radius}"
        )
        return endpoint

    raise RuntimeError(f"No valid endpoint found near renal hilum label {hilum_label} within radius {max_radius}.")


