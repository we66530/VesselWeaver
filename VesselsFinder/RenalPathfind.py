def run_renal_pathfinder(case_id: str) -> Tuple[Path, Path]:
    ct_path = build_roi_ct_path(case_id)
    start_points_path = build_start_points_path(case_id)
    renal_hilum_path = build_renal_hilum_path(case_id)
    output_path = build_output_path(case_id)
    summary_path = build_summary_path(case_id)

    print("=" * 100)
    print("[RENAL ARTERY / VEIN PATHFINDER - RUN.PY VERSION]")
    print(f"[CASE ID      ] {case_id}")
    print(f"[ROI CT       ] {ct_path}")
    print(f"[START POINTS ] {start_points_path}")
    print(f"[RENAL HILUM  ] {renal_hilum_path}")
    print(f"[OUTPUT       ] {output_path}")
    print("=" * 100)

    ensure_dir(output_path.parent)

    if SKIP_IF_FINAL_EXISTS and output_path.exists() and summary_path.exists():
        print(f"[SKIP] Output already exists: {output_path}")
        print(f"[SKIP] Summary already exists: {summary_path}")
        return output_path, summary_path

    ct_img, ct_data = load_nifti(ct_path)
    start_img_raw, start_data_raw = load_nifti(start_points_path)
    hilum_img_raw, hilum_data_raw = load_nifti(renal_hilum_path)

    _, start_data = align_mask_to_ct_if_needed(start_img_raw, start_data_raw, ct_img, "renal start points")
    _, hilum_data = align_mask_to_ct_if_needed(hilum_img_raw, hilum_data_raw, ct_img, "renal hilum mask")

    print(f"[INFO] ROI_CT shape: {ct_data.shape}")
    print(f"[INFO] ROI_CT HU range: min={ct_data.min():.1f}, max={ct_data.max():.1f}")
    print(f"[INFO] Start labels present: {[int(x) for x in np.unique(np.rint(start_data).astype(np.int32)) if x > 0]}")
    print(f"[INFO] Hilum labels present: {[int(x) for x in np.unique(np.rint(hilum_data).astype(np.int32)) if x > 0]}")

    traced_paths: List[List[Tuple[int, int, int]]] = []
    labeled_paths: List[Tuple[int, List[Tuple[int, int, int]]]] = []
    records: List[Dict] = []

    for task in RENAL_PATH_TASKS:
        print("\n" + "=" * 100)
        print(f"[PATH TASK] {task.name}")
        print(
            f"[INFO] start_label={task.start_label}, direction={task.direction}, "
            f"target_hilum_label={task.target_hilum_label}, output_label={task.output_label}"
        )
        print("=" * 100)

        start_point = select_start_point_from_label(
            ct_data=ct_data,
            start_data=start_data,
            start_label=task.start_label,
            direction=task.direction,
        )

        endpoint = find_valid_endpoint_for_hilum_label(
            ct_data=ct_data,
            hilum_data=hilum_data,
            hilum_label=task.target_hilum_label,
            max_radius=ENDPOINT_SEARCH_MAX_RADIUS,
            distance_tolerance=ENDPOINT_DISTANCE_TOLERANCE,
        )

        exclusion_mask = build_spacing_exclusion_from_paths(
            shape=ct_data.shape,
            paths=traced_paths,
            spacing_voxels=MIN_PATH_DISTANCE_VOXELS,
        )

        path, cost = dijkstra_pathfinder(
            ct_data=ct_data,
            start=start_point,
            end=endpoint,
            exclusion_mask=exclusion_mask,
        )

        spacing_status = "strict_spacing_ok"
        if path is None:
            print(f"[WARNING] Strict spacing failed for {task.name}. Retrying without spacing exclusion.")
            path, cost = dijkstra_pathfinder(
                ct_data=ct_data,
                start=start_point,
                end=endpoint,
                exclusion_mask=None,
            )
            spacing_status = "retried_without_spacing"

        if path is None:
            raise RuntimeError(f"Path search failed for task: {task.name}")

        traced_paths.append(path)
        labeled_paths.append((task.output_label, path))

        path_hu = np.array([ct_data[p] for p in path], dtype=np.float64)
        print(
            f"[OK] {task.name}: path_voxels={len(path):,}, cost={cost:.3f}, "
            f"mean_HU={float(path_hu.mean()):.2f}, min_HU={float(path_hu.min()):.2f}, "
            f"max_HU={float(path_hu.max()):.2f}, spacing_status={spacing_status}"
        )

        records.append({
            "task_name": task.name,
            "start_label": task.start_label,
            "direction": task.direction,
            "target_hilum_label": task.target_hilum_label,
            "output_label": task.output_label,
            "start_x": start_point[0],
            "start_y": start_point[1],
            "start_z": start_point[2],
            "start_hu": float(ct_data[start_point]),
            "end_x": endpoint[0],
            "end_y": endpoint[1],
            "end_z": endpoint[2],
            "end_hu": float(ct_data[endpoint]),
            "cost": float(cost),
            "path_voxels": int(len(path)),
            "mean_hu": float(path_hu.mean()),
            "min_hu": float(path_hu.min()),
            "max_hu": float(path_hu.max()),
            "spacing_status": spacing_status,
        })

    output_labelmap = render_paths_to_labelmap(ct_data.shape, labeled_paths)

    out_header = ct_img.header.copy()
    out_header.set_data_dtype(np.uint16)

    out_img = nib.Nifti1Image(
        output_labelmap.astype(np.uint16),
        affine=ct_img.affine,
        header=out_header,
    )
    nib.save(out_img, str(output_path))

    if not output_path.exists():
        raise RuntimeError(f"Output was not created: {output_path}")

    with open(summary_path, "w", encoding="utf-8") as f:
        f.write(f"case_id\t{case_id}\n")
        f.write(f"roi_ct_path\t{ct_path}\n")
        f.write(f"start_points_path\t{start_points_path}\n")
        f.write(f"renal_hilum_path\t{renal_hilum_path}\n")
        f.write(f"output_path\t{output_path}\n")
        f.write(f"invalid_hu_value\t{INVALID_HU_VALUE}\n")
        f.write(f"min_path_distance_voxels\t{MIN_PATH_DISTANCE_VOXELS}\n")
        f.write(f"root_relax_distance\t{ROOT_RELAX_DISTANCE}\n")
        f.write(f"end_relax_distance\t{END_RELAX_DISTANCE}\n")
        f.write(f"use_26_neighbor_pathfinding\t{USE_26_NEIGHBOR_PATHFINDING}\n")
        f.write(f"dijkstra_bbox_margins\t{DIJKSTRA_BBOX_MARGINS}\n")
        f.write("\n")

        header = [
            "task_name",
            "start_label",
            "direction",
            "target_hilum_label",
            "output_label",
            "start_x",
            "start_y",
            "start_z",
            "start_hu",
            "end_x",
            "end_y",
            "end_z",
            "end_hu",
            "cost",
            "path_voxels",
            "mean_hu",
            "min_hu",
            "max_hu",
            "spacing_status",
        ]
        f.write("\t".join(header) + "\n")
        for r in records:
            row = []
            for key in header:
                value = r[key]
                if isinstance(value, float):
                    row.append(f"{value:.6f}")
                else:
                    row.append(str(value))
            f.write("\t".join(row) + "\n")

    print("\n" + "=" * 100)
    print("[DONE] Renal artery/vein pathfinding completed.")
    print(f"[OUTPUT MASK] {output_path}")
    print(f"[SUMMARY    ] {summary_path}")
    print(f"[INFO] Output labels present: {[int(x) for x in np.unique(output_labelmap) if x > 0]}")
    print("=" * 100)

    return output_path, summary_path


